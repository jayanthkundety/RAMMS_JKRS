function concatenate(e) {
    try {
        var array = e.val();
        var value = '';
        if (array.length > 0) {
            $.each(array, function (i, v) {
                if (value != '') {
                    value += ",";
                }
                value += v;

            });
        }
        return value;
    }
    catch{
        return "";
    }

}

